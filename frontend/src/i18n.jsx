const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem('fangcun-language') || 'en');

  const value = useMemo(() => {
    const dictionary = translations[language] ?? translations.en;
    return {
      language,
      setLanguage: (nextLanguage) => {
        localStorage.setItem('fangcun-language', nextLanguage);
        setLanguage(nextLanguage);
      },
      toggleLanguage: () => {
        const nextLanguage = language === 'en' ? 'zh' : 'en';
        localStorage.setItem('fangcun-language', nextLanguage);
        setLanguage(nextLanguage);
      },
      t: (key, params) => {
        const value = key.split('.').reduce((acc, part) => acc?.[part], dictionary);
        const fallback = key.split('.').reduce((acc, part) => acc?.[part], translations.en);
        const resolved = value ?? fallback ?? key;
        return typeof resolved === 'function' ? resolved(params ?? {}) : resolved;
      },
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}
